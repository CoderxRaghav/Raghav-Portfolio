const connectDB = require("./config/db");

dotenv.config();

const app = express();
app.use(cors());
app.use(express.json());
app.use("/api/analytics", analyticsRoutes);

// CONNECT DATABASE
connectDB();

app.get("/", (req, res) => {
  res.send("API running");
});

